import { symbols } from '../../symbols';
import { syntaxAnalysis } from '../syntaxAnalysis';

describe('syntaxAnalysis', () => {
    it('should handle label and goto', () => {
        const tokens = [
            symbols.program,
            symbols.identificador,
            symbols[';'],
            symbols.var,
            symbols.identificador,
            symbols[','],
            symbols.identificador,
            symbols[','],
            symbols.identificador,
            symbols[','],
            symbols.identificador,
            symbols[':'],
            symbols.integer,
            symbols[';'],
            symbols.begin,
            symbols.identificador,
            symbols[':='],
            symbols.inteiro,
            symbols[';'],
            symbols.identificador,
            symbols[':='],
            symbols.inteiro,
            symbols[';'],
            symbols.while,
            symbols.inteiro,
            symbols['='],
            symbols.inteiro,
            symbols.do,
            symbols.begin,
            symbols.readln,
            symbols['('],
            symbols.identificador,
            symbols[')'],
            symbols[';'],
            symbols.if,
            symbols.identificador,
            symbols['<'],
            symbols.inteiro,
            symbols.then,
            symbols.goto,
            symbols.identificador,
            symbols[';'],
            symbols.identificador,
            symbols[':='],
            symbols.identificador,
            symbols['+'],
            symbols.identificador,
            symbols[';'],
            symbols.identificador,
            symbols[':='],
            symbols.identificador,
            symbols['+'],
            symbols.inteiro,
            symbols[';'],
            symbols.end,
            symbols[';'],
            symbols.identificador,
            symbols[':'],
            symbols.identificador,
            symbols[':='],
            symbols.identificador,
            symbols['/'],
            symbols.identificador,
            symbols[';'],
            symbols.writeln,
            symbols['('],
            symbols.literal,
            symbols[','],
            symbols.identificador,
            symbols[')'],
            symbols[';'],
            symbols.end,
            symbols['.']
        ].reverse();
        expect(() => syntaxAnalysis(tokens)).not.toThrow();
        expect(tokens).toEqual([]);
    });
});
